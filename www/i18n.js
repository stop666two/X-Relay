// 当前语言
let lang = (navigator.language || 'en').startsWith('zh') ? 'zh-CN' : 'en';

function t(key, params) {
  let s = (I18N[lang] && I18N[lang][key]) || I18N['en'][key] || key;
  if (params) {
    Object.keys(params).forEach(k => { s = s.replace('{' + k + '}', params[k]); });
  }
  return s;
}

function setLang(l) {
  if (I18N[l]) { lang = l; localStorage.setItem('xrelay-lang', l); }
}

// 恢复保存的语言
const saved = localStorage.getItem('xrelay-lang');
if (saved && I18N[saved]) lang = saved;
